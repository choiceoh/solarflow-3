import * as React from "react"

import { cn } from "@/lib/utils"
import { cardClasses } from "./card-variants"

function Card({
  className,
  size = "default",
  ...props
}: React.ComponentProps<"div"> & { size?: "default" | "sm" }) {
  return (
    <div
      data-slot="card"
      data-size={size}
      className={cn(cardClasses.root, className)}
      {...props}
    />
  )
}

